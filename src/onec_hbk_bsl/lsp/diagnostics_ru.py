def translate_message(code: str, original_message: str) -> str:
    """
    Возвращает русское сообщение для диагностики.

    Для известных правил подставляет русский заголовок и рекомендацию.
    Для неизвестных пытается вернуть понятную русскую формулировку
    на основе метаданных правила.
    """
    info = DIAGNOSTICS_RU.get(code)
    if not info:
        return _fallback_ru_message(code, original_message)

    title = info["title"]
    hint = info.get("hint", "")

    # Извлекаем «специфическое» значение из английского сообщения
    # (имя переменной, длину метода и т.п.), если оно есть.
    specific = _extract_specific(code, original_message)

    if specific:
        msg = f"{title}: {specific}"
    else:
        msg = title

    if hint:
        msg += f"\n💡 {hint}"

    return msg


def localize_rule_title(code: str) -> str:
    """Русский заголовок правила для UI/CodeAction (best-effort)."""
    info = DIAGNOSTICS_RU.get(code)
    if info and info.get("title"):
        return info["title"]
    md = RULE_METADATA.get(code, {})
    desc = str(md.get("description", "")).strip()
    if desc:
        return _localize_generic_text(desc)
    return code


# Шаблоны для извлечения специфических значений из английских сообщений.
# Каждый элемент: (code, regex_pattern, lambda для форматирования)
import re as _re  # noqa: E402

_EXTRACT_PATTERNS: list[tuple[str, _re.Pattern[str], object]] = [
    ("BSL002", _re.compile(r"'([^']+)' is (\d+) lines"), lambda m: f"'{m.group(1)}' — {m.group(2)} строк"),
    ("BSL003", _re.compile(r"'([^']+)' is in API"), lambda m: f"'{m.group(1)}'"),
    ("BSL007", _re.compile(r"variable '([^']+)'", _re.IGNORECASE), lambda m: f"'{m.group(1)}'"),
    ("BSL009", _re.compile(r"variable '([^']+)'", _re.IGNORECASE), lambda m: f"'{m.group(1)}'"),
    ("BSL011", _re.compile(r"'([^']+)'.*?(\d+).*?(\d+)"), lambda m: f"'{m.group(1)}' — {m.group(2)}/{m.group(3)}"),
    ("BSL012", _re.compile(r"'([^']+)'"), lambda m: f"'{m.group(1)}'"),
    ("BSL005", _re.compile(r"'([^']+)'"), lambda m: f"'{m.group(1)}'"),
    ("BSL006", _re.compile(r"'([^']+)'"), lambda m: f"'{m.group(1)}'"),
    ("BSL014", _re.compile(r"(\d+).*?(\d+)"), lambda m: f"{m.group(1)} симв. (макс. {m.group(2)})"),
    ("BSL021", _re.compile(r"parameter '([^']+)'", _re.IGNORECASE), lambda m: f"'{m.group(1)}'"),
    ("BSL029", _re.compile(r"(\d[\d.]*)"), lambda m: f"{m.group(1)}"),
    ("BSL031", _re.compile(r"'([^']+)'.*?(\d+).*?(\d+)"), lambda m: f"'{m.group(1)}' — {m.group(2)}/{m.group(3)} параметров"),
    ("BSL034", _re.compile(r"Variable '([^']+)'", _re.IGNORECASE), lambda m: f"'{m.group(1)}'"),
    ("BSL035", _re.compile(r"'([^']+)'"), lambda m: f"'{m.group(1)}'"),
    ("BSL033", _re.compile(r"'([^']+)'"), lambda m: f"'{m.group(1)}'"),
    ("BSL062", _re.compile(r"'([^']+)'"), lambda m: f"'{m.group(1)}'"),
]


def _extract_specific(code: str, message: str) -> str:
    """Извлекает специфическое значение (имя, число) из английского сообщения."""
    for pat_code, pattern, formatter in _EXTRACT_PATTERNS:
        if pat_code != code:
            continue
        m = pattern.search(message)
        if m:
            try:
                return formatter(m)  # type: ignore[operator]
            except Exception:
                pass
    return ""


def _fallback_ru_message(code: str, original_message: str) -> str:
    md = RULE_METADATA.get(code, {})
    desc = str(md.get("description", "")).strip()
    if desc:
        return _localize_generic_text(desc)
    return _localize_generic_text(original_message)


def _localize_generic_text(text: str) -> str:
    """Упрощенная локализация типовых английских формулировок."""
    replacements = (
        ("must be", "должен быть"),
        ("should be", "должен быть"),
        ("is not", "не"),
        ("is never", "никогда не"),
        ("inside", "внутри"),
        ("outside", "вне"),
        ("found", "обнаружен"),
        ("missing", "отсутствует"),
        ("duplicate", "дублирующийся"),
        ("duplicates", "дублирует"),
        ("without", "без"),
        ("with", "с"),
        ("before", "перед"),
        ("after", "после"),
        ("inside a loop", "внутри цикла"),
        ("in a loop", "в цикле"),
        ("security risk", "риск безопасности"),
        ("readability", "читаемость"),
        ("reduces readability", "снижает читаемость"),
        ("likely a mistake", "вероятная ошибка"),
        ("for consistency", "для единообразия"),
    )
    out = text
    for src, dst in replacements:
        out = out.replace(src, dst)
    return out
